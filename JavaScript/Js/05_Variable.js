/* in js variable we define three type
let,var,const*/
// Example with var
var x = 10;
console.log("var x =", x);  // Output: 10

x = 20;  // You can reassign
console.log("updated var x =", x);  // Output: 20

// Example with let
let y = 30;
console.log("let y =", y);  // Output: 30

y = 40;  // You can reassign
console.log("updated let y =", y);  // Output: 40

// Example with const
const z = 50;
console.log("const z =", z);  // Output: 50

